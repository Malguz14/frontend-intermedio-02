/**
 * Obtiene el mayor número de los parámetros recibidos utilizando `math.max`
 * @param {number} n1 Número 1
 * @param {number} n2 Número 2
 * @param {number} n3 Número 3
 * @param {number} n4 Número 4
 * @returns El número mayor
 */
const mayorEntre4v4 = (n1, n2, n3, n4) => {
  return Math.max(n1, n2, n3, n4);
};

module.exports = mayorEntre4v4;
